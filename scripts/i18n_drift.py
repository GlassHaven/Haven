def insert_into(module: str, entries):
    en_path = REPO / module / "src/main/res/values/strings.xml"
    en_text = en_path.read_text()
    # Verify every key actually exists in EN before we add it elsewhere.
    en_keys = set(re.findall(r'<string\s+name="([^"]+)"', en_text))
    for key, _ in entries:
        if key not in en_keys:
            raise SystemExit(f"key {key} not in {module} EN source")
    for i, locale in enumerate(LOCALES):
        loc_path = REPO / module / "src/main/res" / f"values-{locale}/strings.xml"
        text = loc_path.read_text()
        existing = set(re.findall(r'<string\s+name="([^"]+)"', text))
        new_lines = []
        for key, translations in entries:
            if key in existing:
                continue
            value = translations[i]
            new_lines.append(f'    <string name="{key}">{value}</string>')
        if not new_lines:
            print(f"  {module} values-{locale}: nothing to add")
            continue
        # Insert just before </resources>.
        block = "    <!-- Backfilled translations (i18n drift fix) -->\n" + "\n".join(new_lines) + "\n"
        new_text = re.sub(r'(\n)?</resources>\s*$', "\n" + block + "</resources>\n", text)
        loc_path.write_text(new_text)
        print(f"  {module} values-{locale}: +{len(new_lines)} strings")


def main():
    insert_into("feature/sftp", SFTP)
    insert_into("feature/terminal", TERMINAL)
    insert_into("feature/connections", CONNECTIONS)
    insert_into("feature/settings", SETTINGS)


if __name__ == "__main__":
    main()
